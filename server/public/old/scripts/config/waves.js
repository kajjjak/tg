_dictArray2dictByLevelWave = function(item_array){
        var items = {};
        // lets group this array into levels and waves
        for (var i in item_array){
                var item = item_array[i];
                if(!item.disabled){
                        var l = item.level-1;
                        var w = item.wave-1;
                        if(!items[l]){ items[l] = {};}
                        if(!items[l][w]){ items[l][w] = [];}
                        items[l][w].push(item);
                }
        }
        var levels = [];
        // in this loop we replace the dictionary with arrays
        for (var i in items){
                if(!levels[i]){ levels.push([]); }
                for(var j in items[i])
                if(!levels[i][j]){ levels[i].push(items[i][j]);}
                //levels[i][j].unshift([{message:_lang["attack_wave_startingin"], delay:0}]);
        }
        return levels;
};

