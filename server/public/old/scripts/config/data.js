function buildLedgerItemTransactions(ledger_item_transaction){
    /*
    "iditem-diamons00": {"account":"resource", "name": "Startup of diamonds", "diamonds":155, "time":20, "iap":-2},
    "iditem-iap": {"account":"resource", "name": "IAP", "iap":20},
    "iditem-wood-startup": {"account":"resource", "name": "IAP wood", "wood":100, "diamonds":-1, "iap":-1},    
    */
    for (var i in ii_iap){
        var iap = ii_iap[i];
        ledger_item_transaction[iap.iap] = {
            "account": "resource",
            "name": iap.name,
            "iap": iap.iap_count,
            "description": iap.description
        };
        if (iap.currency){
            ledger_item_transaction[iap.iap][iap.currency] = iap.value;
        }
        if (iap.time){
            ledger_item_transaction[iap.iap]["time"] = iap.time;    
        }
        if (iap.diamonds != undefined){
            ledger_item_transaction[iap.iap]["diamonds"] = iap.diamonds;    
        }        
    }
    return ledger_item_transaction;
}

